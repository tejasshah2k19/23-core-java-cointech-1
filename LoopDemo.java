public class LoopDemo {
    public static void main(String[] args) {
        int i;
        for (i = 1; i <= 10; i++) { // i = i + 1

            System.out.println("Java" + i);

        }

        System.out.println("i => " + i);

        // --------------------------------
        i = 1;
        while (i <= 10) {
            System.out.println("JAVA" + i);
            i++;
        }
    }
}
